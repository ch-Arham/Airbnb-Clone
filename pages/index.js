// SSR --- happeing at server side before sending all stuff to the user
// This function gets called at build time on server-side.
// It won't be called on client-side, so you can even do
// direct database queries.
//getStaticProps always return the object and, it executes before the page component.
export async function getStaticProps() {
  const exploreData = await fetch('https://links.papareact.com/pyp').then(res=>res.json());
  
  const cardsData = await fetch('https://links.papareact.com/zp1').then(res=>res.json());

  return {
    props: {exploreData,cardsData}, // will be passed to the page component as props
  };
  
};
